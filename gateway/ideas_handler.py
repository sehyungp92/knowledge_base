_DEVELOP_PROMPT = """\
You are developing an idea from a knowledge engine's tournament pipeline.
Your job is to make this idea more concrete, actionable, and grounded.
{voice_section}
## Idea
{idea_text}

## Metadata
- Type: {idea_type}
- Testability: {testability}
- Grounding: {grounding}

## User Notes
{user_notes}

## Related Library Evidence
{evidence_text}

## Instructions

1. **Expand**: Flesh out the idea into a concrete proposal. What specifically would this look like?
2. **Ground**: Connect it more deeply to the evidence. What specific claims support or challenge it?
3. **Test**: Define 2-3 concrete, actionable tests or experiments that could validate or invalidate this idea.
4. **Risks**: What could go wrong? What assumptions might be wrong?
5. **Next steps**: What would the first concrete step be to pursue this idea?

Be specific and practical. Reference sources and claims by ID where possible.
"""


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def _get_idea(idea_id: str) -> dict | None:
    from reading_app.db import get_conn
    with get_conn() as conn:
        return conn.execute(
            "SELECT * FROM ideas WHERE id = %s", (idea_id,)
        ).fetchone()


def _get_idea_status(idea: dict) -> str:
    ctx = idea.get("generation_context") or {}
    if isinstance(ctx, str):
        try:
            ctx = json.loads(ctx)
        except (json.JSONDecodeError, TypeError):
            ctx = {}
    return ctx.get("status", "new")


def _format_grounding(grounding) -> str:
    if not grounding:
        return "(none)"
    if isinstance(grounding, str):
        try:
            grounding = json.loads(grounding)
        except (json.JSONDecodeError, TypeError):
            return grounding[:200]
    if isinstance(grounding, list):
        parts = []
        for g in grounding[:5]:
            if isinstance(g, dict):
                parts.append(f"- [{g.get('source_id', '?')}] {g.get('snippet', '')[:100]}")
            else:
                parts.append(f"- {str(g)[:100]}")
        return "\n".join(parts)
    return str(grounding)[:200]
