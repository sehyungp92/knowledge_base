"""Direct Python handler for /ideas jobs.

Idea lifecycle manager — list, rate, develop, and track ideas
generated by /reflect deep.
"""

from __future__ import annotations

import json
import re
import time
from typing import Callable

import structlog

from gateway.models import Event, Job

logger = structlog.get_logger(__name__)


def _load_voice(config) -> str:
    """Load soul.md voice preamble for narrative output."""
    try:
        from pathlib import Path
        from reading_app.memory import MemorySystem
        ms = MemorySystem(Path(config.memory_path))
        return ms.load_voice()
    except Exception:
        return ""


# ---------------------------------------------------------------------------
# Entry point
# ---------------------------------------------------------------------------

def handle_ideas_job(
    event: Event,
    job: Job,
    config,
    executor,
    *,
    on_progress: Callable[[str], None] | None = None,
) -> str:
    """Run /ideas directly."""
    text = event.payload.get("text", "")
    log = logger.bind(job_id=job.id)
    log.info("ideas_handler_start")
    t0 = time.monotonic()

    from reading_app.db import ensure_pool
    ensure_pool()

    subcmd, args = _parse_command(text)
    log = log.bind(subcmd=subcmd)

    if subcmd == "list":
        result = _handle_list(args, log)
    elif subcmd == "rate":
        result = _handle_rate(args, log)
    elif subcmd == "note":
        result = _handle_note(args, log)
    elif subcmd == "develop":
        result = _handle_develop(args, executor, on_progress, log, config=config)
    elif subcmd == "status":
        result = _handle_status_change(args, log)
    elif subcmd == "view":
        result = _handle_view(args, log)
    else:
        result = _handle_list(args, log)  # default to list

    elapsed = time.monotonic() - t0
    log.info("ideas_handler_complete", elapsed_s=round(elapsed, 1))
    return result


# ---------------------------------------------------------------------------
# Command parser
# ---------------------------------------------------------------------------

def _parse_command(text: str) -> tuple[str, str]:
    """Parse '/ideas [subcmd] [args]'.

    Returns (subcmd, remaining_args).
    """
    cleaned = text.strip()
    for prefix in ("/ideas ", "/ideas"):
        if cleaned.lower().startswith(prefix):
            cleaned = cleaned[len(prefix):].strip()
            break

    if not cleaned:
        return "list", ""

    parts = cleaned.split(None, 1)
    subcmd = parts[0].lower()
    args = parts[1] if len(parts) > 1 else ""

    known = {"list", "rate", "note", "develop", "status", "view"}
    if subcmd in known:
        return subcmd, args

    # If not a known subcommand, treat as filter for list
    return "list", cleaned


